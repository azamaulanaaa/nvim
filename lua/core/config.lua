local opt = vim.opt

-- file format
opt.fileformats = "unix"

-- indentation
opt.expandtab = true
opt.shiftwidth = 2
opt.smartindent = true
opt.tabstop = 2
opt.softtabstop = 2

-- Numbers
opt.number = true
opt.relativenumber = true
opt.numberwidth = 2
opt.ruler = false

-- no wrap
opt.wrap = false

-- gap when scroll
opt.scrolloff = 4
opt.sidescrolloff = 12

-- clipboard
opt.clipboard = "unnamedplus"
